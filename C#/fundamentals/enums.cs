/* Enums - classe especial que representa um grupo de constantes */

/* Voce define enumerações quando precisa definir um tipo que pode ter um conjunto de valores discretos.
Eles usam um dos tipos de valor integral como armazenamento subjacente. 
Eles fornecem significado semântico aos valores discretos. */

// ex:

enum Level 
{
  Low,
  Medium,
  High
}