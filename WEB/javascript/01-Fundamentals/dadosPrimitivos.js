/* Os dados primitivos são identificadores de objetos */

/*  String = texto
    Number = números
    BigInt = Números grandes de qualquer formato
    Boolean = verdadeiro ou falso
    Undefined = variável criada mas sem valor
    Symbol = símbolo 
    Null = uma referência que aponta para um local não existente. Em certos casos null não é primitivo. Todo objeto é derivado de null.
*/